def get_about_content() -> str:
    """About 탭에 표시될 마크다운 컨텐츠 반환"""
    return """
# 🔗 GGG Idea Studio란?

**"당신의 모든 경험이 다음 혁신의 시작점입니다"**

---

## 💡 핵심 철학: Connecting the Dots

스티브 잡스는 말했습니다. *"미래를 내다보며 점들을 연결할 수는 없다. 과거를 돌아보며 점들을 연결할 수 있을 뿐이다."*

GGG Idea Studio는 이 철학을 현재로 가져와, **당신의 과거 경험들(점들)을 의도적으로 연결하여 미래의 혁신을 만들어내는 도구**입니다.

## 🎯 우리의 믿음

- **모든 경험은 자산이다**: 실패한 프로젝트도, 배운 기술도, 참여한 활동도 모두 소중한 '노드'입니다
- **연결이 창조를 만든다**: 개별 경험들이 만나면 예상치 못한 혁신적 아이디어가 탄생합니다
- **AI는 연결의 촉매제다**: 인공지능이 당신의 노드들 사이의 숨겨진 패턴을 발견해줍니다
- **맥락이 방향을 결정한다**: 같은 노드라도 다른 맥락에서는 전혀 다른 아이디어로 발전할 수 있습니다

## 🔄 작동 원리

### 1️⃣ **노드 수집 단계**
당신의 프로젝트, 경험, 기술 스택을 '노드'로 체계화합니다. 
각 노드는 미래 아이디어의 재료가 됩니다.

### 2️⃣ **맥락 설정 단계**  
- **Domain**: 도전하고 싶은 분야
- **Context**: 문제의 배경과 환경
- **Igniter**: 아이디어의 핵심 방향성

### 3️⃣ **연결 선택 단계**
수많은 노드 중에서 현재 맥락에 맞는 노드들을 선별합니다.
필터링을 통해 최적의 조합을 찾아냅니다.

### 4️⃣ **아이디어 창출 단계**
AI가 선택된 노드들과 맥락을 분석하여 
**연결의 근거**와 함께 구체적인 아이디어를 제안합니다.

## 🌈 이런 분들을 위한 도구입니다

- **공모전 참가자**: 내 경험을 활용한 차별화된 아이디어가 필요한 분
- **창업 준비생**: 기존 지식을 새로운 영역에 응용하고 싶은 분  
- **기획자**: 다양한 배경을 가진 팀원들의 시너지를 찾고 싶은 분
- **학습자**: 배운 것들을 실제 문제 해결에 연결하고 싶은 분

## 🎨 GGG Idea Studio만의 특별함

### 🔍 **투명한 창작 과정**
단순히 아이디어만 제공하는 것이 아니라, **왜 이 아이디어가 나왔는지 그 연결 과정**까지 보여줍니다.

### 🎯 **개인화된 혁신**  
당신만의 독특한 경험 조합으로 **오직 당신만이 낼 수 있는 아이디어**를 만들어냅니다.

### 🔄 **지속적인 자산 축적**
새로운 경험이 쌓일 때마다 노드를 추가하여 **아이디어 창출 역량이 계속 성장**합니다.

### 🤝 **협업 친화적**
팀원들의 노드를 테넌트별로 관리하여 **집단 지성의 힘**을 발휘할 수 있습니다.

---

## 🚀 시작해보세요!

**당신의 첫 번째 노드부터 입력해보세요.** 

작은 경험이라고 생각되는 것도, 언젠가는 세상을 바꿀 아이디어의 핵심 재료가 될 수 있습니다.

*"혁신은 1,000가지 것들을 말하는 것이 아니라, 1,000가지 것들을 연결하는 것이다"* - 스티브 잡스

**당신만의 연결, 당신만의 혁신을 시작하세요.**
"""


